
def addition(number1, number2):
    result = number1 + number2
    return result


def subtraction(number1, number2):
    result = number1 - number2
    return result


def multiplication(number1, number2):
    result = number1 * number2
    return result


def division(number1, number2):
    result = None
    if (number2 != 0):
        result = float(number1) / number2
    return result
